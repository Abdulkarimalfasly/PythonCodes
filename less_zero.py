def lessThanOrEqualToZero(num):
    # Check if the number is less than or equal to zero
    if num <= 0:
        return True  # Return True if the condition is met
    else:
        return False  # Return False if the condition is not met

# Examples:
print(lessThanOrEqualToZero(5))   # ➞ False
print(lessThanOrEqualToZero(0))   # ➞ True
print(lessThanOrEqualToZero(-2))  # ➞ True
